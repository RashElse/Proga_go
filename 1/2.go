package main

import "fmt"

func main() {
	var integerVar int = 42
	var floatVar float64 = 3.14
	var stringVar string = "Привет, Go!"
	var boolVar bool = true

	fmt.Println("Переменная типа int:", integerVar)
	fmt.Println("Переменная типа float64:", floatVar)
	fmt.Println("Переменная типа string:", stringVar)
	fmt.Println("Переменная типа bool:", boolVar)
}
